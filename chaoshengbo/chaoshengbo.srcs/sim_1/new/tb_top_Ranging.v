`timescale 1ns / 1ps

module tb_top_Ranging();

    // 1. 声明输入输出信号
    reg clk_100m;
    reg RST;
    reg Echo;

    wire Trig;

    // 2. 实例化你刚才综合通过的顶层模块
    top_Ranging uut (
        .clk_100m(clk_100m),
        .RST(RST),
        .Echo(Echo),
        .Trig(Trig)
    );

    // 3. 产生 100MHz 系统时钟 (周期 10ns -> 5ns 高, 5ns 低)
    initial begin
        clk_100m = 0;
        forever #5 clk_100m = ~clk_100m;
    end

    // =================================================================
    // ★ 核心任务：模拟超声波传感器返回 Echo 信号
    // =================================================================
    task send_echo;
        input [15:0] target_cm;
        integer echo_time_ns;
        begin
            // 等待 FPGA 发出 Trig 触发脉冲的下降沿 (10us脉冲结束)
            @(negedge Trig);
            
            // 模拟物理世界中，超声波模块内部处理的延迟 (约 200us)
            #200_000; 
            
            // 计算 Echo 应该保持高电平的时间 (单位: ns)
            // 公式: 1cm = 58.8us = 58800ns
            echo_time_ns = target_cm * 58800; 
            
            // 产生 Echo 物理信号
            Echo = 1'b1;
            #(echo_time_ns);
            Echo = 1'b0;
            
            // 等待 FPGA 内部状态机完成计算 (多等一小会儿)
            #1000; 
            
            // 在 Vivado 控制台自动打印比对结果
            // 注意：因为 data 存的是 BCD 码，用 %h (十六进制) 打印出来，眼睛看起来正好是十进制！
            $display("Time: %10t | 注入测试距离: %3d cm | FPGA内部计算结果: %h", $time, target_cm, uut.u_calc.data);
        end
    endtask

    // =================================================================
    // ★ 主测试流程：10 个精心设计的测试点
    // =================================================================
    initial begin
        // 初始化引脚状态
        RST = 1'b1;
        Echo = 1'b0;
        
        // 产生系统复位 (模拟按键按下并松开)
        #100;
        RST = 1'b0;
        #200;
        RST = 1'b1;
        
        $display("==================================================");
        $display("--- 仿真开始：超声波测距 10 个测试点 ---");
        $display("==================================================");
        
        // 【测试点 1】: 极近距离，测试最小阈值响应
        send_echo(2);
        
        // 【测试点 2】: 个位数，测试基本计算
        send_echo(9);
        
        // 【测试点 3】: 进位边界，测试 BCD 码个位向十位进位
        send_echo(10);
        
        // 【测试点 4】: 普通距离，(你后续血条系统的标准判定范围)
        send_echo(45);
        
        // 【测试点 5】: 进位边界，测试 BCD 码准备向百位进位
        send_echo(99);
        
        // 【测试点 6】: 百位进位，测试十位向百位进位
        send_echo(100);
        
        // 【测试点 7】: 三位数距离，测试多位联合
        send_echo(125);
        
        // 【测试点 8】: 较远距离，测试计数器累加稳定性
        send_echo(255);
        
        // 【测试点 9】: 极限距离 (20ms周期下最大测距约为340cm，300cm为安全极限)
        send_echo(300);
        
        // 【测试点 10】: 恢复测试，极限距离后能否正常测算近距离
        send_echo(15);
        
        $display("==================================================");
        $display("--- 仿真结束，所有 10 个测试点跑完 ---");
        $display("==================================================");
        
        // 停止仿真
        #1000;
        $stop;
    end

endmodule